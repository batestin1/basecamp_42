int	ft_str_is_alpha(char *str)
{
	int	i;
	int	result;

	result = 1;
	i = 0;
	while (*(str + i) != '\0')
	{
		if (
			(*(str + i) < 65 || *(str + i) > 90)
			&& (*(str + i) < 97 || *(str + i) > 122))
		{
			result = 0;
		}
		i++;
	}
	return (result);
}
