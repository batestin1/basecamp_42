char	*ft_strlowcase(char *str)
{
	char	*ptr;
	int		i;

	ptr = str;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (ptr);
}
