char	*ft_strcpy(char *dest, char *src)
{
	char	*ptr;
	int		i;

	ptr = dest;
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (ptr);
}
